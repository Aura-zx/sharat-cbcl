function out=rgb2e(img)
	if(size(img)~=3)
		error('Need a rgb image');
	end;
	out(:,:,1) = 0.002358*img(:,:,1)+0.025174*img(:,:,2)+0.010821*img(:,:,3);
	out(:,:,2) = 0.011943*img(:,:,1)+0.001715*img(:,:,2)-0.013994*img(:,:,3);
	out(:,:,3) = 0.013743*img(:,:,1)-0.023965*img(:,:,2)+0.00657*img(:,:,3);
%end function
